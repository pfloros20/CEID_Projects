DROP DATABASE IF EXISTS ESHOP;
CREATE DATABASE ESHOP;
USE ESHOP;

CREATE TABLE `CPU`(
	cpu_model VARCHAR(20) NOT NULL,
	cpu_company ENUM('AMD','INTEL') NOT NULL,
	cpu_frequency FLOAT(2,1) UNSIGNED NOT NULL,				-- IN GHz
	cpu_cores TINYINT UNSIGNED NOT NULL,
	cpu_threads TINYINT UNSIGNED NOT NULL,
	cpu_socket VARCHAR(20) NOT NULL,
	cpu_l1cache_size SMALLINT UNSIGNED NOT NULL,			-- IN KB
	cpu_tdp TINYINT UNSIGNED NOT NULL,						-- TDP=Thermal Design Power (Power Consumption)
	cpu_price FLOAT(6,2) UNSIGNED NOT NULL,
	PRIMARY KEY(cpu_model)
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `MOTHERBOARD`(
	mobo_model VARCHAR(20) NOT NULL,
	mobo_company VARCHAR(20) NOT NULL,
	mobo_socket VARCHAR(20) NOT NULL,
	mobo_ram_frequency SMALLINT UNSIGNED NOT NULL,			-- IN MHz
	mobo_slot_number TINYINT UNSIGNED NOT NULL,
	mobo_ram_type ENUM('DDR3','DDR4','DDR3 SO-DIMM','DDR4 SO-DIMM') NOT NULL,
	mobo_ram_cap SMALLINT UNSIGNED NOT NULL,				-- IN GB
	mobo_size ENUM('uATX/MicroATX','ATX','ExtendedATX','MiniITX','SSI','Other') NOT NULL,
	mobo_price FLOAT(6,2) UNSIGNED NOT NULL,
	PRIMARY KEY(mobo_model)
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `GPU`(
	gpu_model VARCHAR(20) NOT NULL,
	gpu_supplier VARCHAR(20) NOT NULL,
	gpu_company ENUM('AMD','NVIDIA') NOT NULL,
	gpu_connection_number TINYINT UNSIGNED NOT NULL,
	gpu_connection_type SET('DVI-D','DVI-I','VGA','Mini HDMI','HDMI','Mini DisplayPort','DisplayPort') NOT NULL,
	gpu_ram_size TINYINT UNSIGNED NOT NULL,					-- IN GB
	gpu_ram_type ENUM('GDDR3','GDDR4','GDDR5','GDDR6') NOT NULL,
	gpu_frequency SMALLINT UNSIGNED NOT NULL,				-- IN MHz
	gpu_ram_frequency SMALLINT UNSIGNED NOT NULL,			-- IN MHz
	gpu_system_power SMALLINT UNSIGNED NOT NULL,			-- IN Watts
	gpu_price FLOAT(6,2) UNSIGNED NOT NULL,
	PRIMARY KEY(gpu_model)
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `PSU`(
	psu_model VARCHAR(20) NOT NULL,
	psu_company VARCHAR(20) NOT NULL,
	psu_power SMALLINT UNSIGNED NOT NULL,					-- IN Watts
	psu_price FLOAT(6,2) UNSIGNED NOT NULL,
	PRIMARY KEY(psu_model)
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `RAM`(
	ram_model VARCHAR(20) NOT NULL,
	ram_company VARCHAR(20) NOT NULL,
	ram_type ENUM('DDR3','DDR4','DDR3 SO-DIMM','DDR4 SO-DIMM') NOT NULL,
	ram_frequency SMALLINT UNSIGNED NOT NULL,				-- IN MHz
	ram_size TINYINT UNSIGNED NOT NULL,						-- IN GB
	ram_price FLOAT(6,2) UNSIGNED NOT NULL,
	PRIMARY KEY(ram_model)
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `CASE`(
	case_model VARCHAR(20) NOT NULL,
	case_company VARCHAR(20) NOT NULL,
	case_price FLOAT(6,2) UNSIGNED NOT NULL,
	case_size ENUM('Midi','Mini','Full Tower','Ultra Tower','Micro') NOT NULL,
	case_mobo_size SET('uATX/MicroATX','ATX','ExtendedATX','MiniITX','SSI','Other') NOT NULL,
	PRIMARY KEY(case_model)
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `SSD`(
	ssd_model VARCHAR(20) NOT NULL,
	ssd_company VARCHAR(20) NOT NULL,
	ssd_capacity SMALLINT UNSIGNED NOT NULL,				-- IN GB
	ssd_size VARCHAR(20) NOT NULL,
	ssd_price FLOAT(6,2) UNSIGNED NOT NULL,
	ssd_connection VARCHAR(20) NOT NULL,
	ssd_read_speed SMALLINT UNSIGNED NOT NULL,				-- IN MB/s
	ssd_write_speed SMALLINT UNSIGNED NOT NULL,				-- IN MB/s
	PRIMARY KEY(ssd_model)
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `HDD`(
	hdd_model VARCHAR(20) NOT NULL,
	hdd_company VARCHAR(20) NOT NULL,
	hdd_capacity TINYINT UNSIGNED NOT NULL,					-- IN TB
	hdd_size VARCHAR(20) NOT NULL,
	hdd_price FLOAT(6,2) UNSIGNED NOT NULL,
	hdd_connection VARCHAR(20) NOT NULL,
	hdd_cache SMALLINT UNSIGNED NOT NULL,					-- IN MB
	hdd_rpm SMALLINT UNSIGNED NOT NULL,
	PRIMARY KEY(hdd_model)
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `extHD`(
	exthd_model VARCHAR(20) NOT NULL,
	exthd_company VARCHAR(20) NOT NULL,
	exthd_capacity TINYINT UNSIGNED NOT NULL,				-- IN TB
	exthd_size VARCHAR(20) NOT NULL,
	exthd_price FLOAT(6,2) UNSIGNED NOT NULL,
	exthd_connection VARCHAR(20) NOT NULL,
	exthd_external_power BOOLEAN NOT NULL,
	PRIMARY KEY(exthd_model)
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `MODELS_COMPABILITY`(
	mobo_model VARCHAR(20) NOT NULL,
	cpu_model VARCHAR(20) NOT NULL
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `CUSTOMER`(
	id INT UNSIGNED AUTO_INCREMENT NOT NULL,
	name VARCHAR(20) NOT NULL,
	surname VARCHAR(20) NOT NULL,
	email VARCHAR(30) NOT NULL,
	phone BIGINT(10) UNSIGNED NOT NULL,
	registration_date DATETIME DEFAULT CURRENT_TIMESTAMP NOT NULL,
	PRIMARY KEY(id)
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `ORDER`(
	id INT UNSIGNED AUTO_INCREMENT NOT NULL,
	customer_id INT UNSIGNED NOT NULL,
	used_together BOOLEAN NOT NULL,
	cpu_model VARCHAR(20) DEFAULT NULL,
	mobo_model VARCHAR(20) DEFAULT NULL,
	gpu_model VARCHAR(20) DEFAULT NULL,
	psu_model VARCHAR(20) DEFAULT NULL,
	ram_model VARCHAR(20) DEFAULT NULL,
	case_model VARCHAR(20) DEFAULT NULL,
	ssd_model VARCHAR(20) DEFAULT NULL,
	hdd_model VARCHAR(20) DEFAULT NULL,
	extHD_model VARCHAR(20) DEFAULT NULL,
	price FLOAT(7,2) UNSIGNED DEFAULT 0 NOT NULL,
	order_date TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
	PRIMARY KEY(id),
	CONSTRAINT ORDER_CUSTOMER_ID
		FOREIGN KEY(customer_id) REFERENCES `CUSTOMER`(id)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CONSTRAINT ORDER_CPU
		FOREIGN KEY(cpu_model) REFERENCES `CPU`(cpu_model)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CONSTRAINT ORDER_MOBO
		FOREIGN KEY(mobo_model) REFERENCES `MOTHERBOARD`(mobo_model)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CONSTRAINT ORDER_GPU
		FOREIGN KEY(gpu_model) REFERENCES `GPU`(gpu_model)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CONSTRAINT ORDER_PSU
		FOREIGN KEY(psu_model) REFERENCES `PSU`(psu_model)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CONSTRAINT ORDER_RAM
		FOREIGN KEY(ram_model) REFERENCES `RAM`(ram_model)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CONSTRAINT ORDER_CASE
		FOREIGN KEY(case_model) REFERENCES `CASE`(case_model)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CONSTRAINT ORDER_SSD
		FOREIGN KEY(ssd_model) REFERENCES `SSD`(ssd_model)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CONSTRAINT ORDER_HDD
		FOREIGN KEY(hdd_model) REFERENCES `HDD`(hdd_model)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CONSTRAINT ORDER_extHD
		FOREIGN KEY(exthd_model) REFERENCES `extHD`(exthd_model)
		ON UPDATE CASCADE ON DELETE CASCADE
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `CUSTOMER_CARD`(
	id INT UNSIGNED AUTO_INCREMENT NOT NULL,
	customer_id INT UNSIGNED NOT NULL,
	available_points INT UNSIGNED NOT NULL DEFAULT 0,
	spent_points INT UNSIGNED NOT NULL DEFAULT 0,
	PRIMARY KEY(id),
	CONSTRAINT CARD_CUSTOMER_ID
		FOREIGN KEY(customer_id) REFERENCES `CUSTOMER`(id)
		ON UPDATE CASCADE ON DELETE CASCADE
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `ADMIN`(
	id INT UNSIGNED AUTO_INCREMENT NOT NULL,
	name VARCHAR(20) NOT NULL,
	surname VARCHAR(20) NOT NULL,
	email VARCHAR(30) NOT NULL,
	bio VARCHAR(250),
	position ENUM('JUNIOR','SENIOR') NOT NULL,
	supervised_by INT UNSIGNED DEFAULT NULL,
	PRIMARY KEY(id),
	CONSTRAINT SUPERVISED_BY
		FOREIGN KEY(supervised_by) REFERENCES `ADMIN`(id)
		ON UPDATE CASCADE ON DELETE CASCADE
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `SALARY`(
	id INT UNSIGNED AUTO_INCREMENT NOT NULL,
	month TINYINT UNSIGNED NOT NULL,
	year SMALLINT UNSIGNED NOT NULL,
	amount FLOAT(7,2) UNSIGNED NOT NULL,
	PRIMARY KEY(id)
)Engine=InnoDB DEFAULT CHARSET=utf8;

CREATE TABLE `GOT_PAID`(
	salary_id INT UNSIGNED NOT NULL,
	admin_id INT UNSIGNED NOT NULL,
	PRIMARY KEY(salary_id,admin_id),
	CONSTRAINT GOT_PAYED_SALARY
		FOREIGN KEY(salary_id) REFERENCES `SALARY`(id)
		ON UPDATE CASCADE ON DELETE CASCADE,
	CONSTRAINT GOT_PAYED_ADMIN
		FOREIGN KEY(admin_id) REFERENCES `ADMIN`(id)
		ON UPDATE CASCADE ON DELETE CASCADE
)Engine=InnoDB DEFAULT CHARSET=utf8;

